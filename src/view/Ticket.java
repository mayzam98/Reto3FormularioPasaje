    private void btAñadirPasajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAñadirPasajeroActionPerformed
        
        String nombre = txNombrePasajero.getText();
        String apellido = txApellidoPasajero.getText();
        String tipoDocumento = cbTipoDocumento.getSelectedItem().toString();
        String numeroDocumento = txNumeroDocumento.getText();
        String sexo = cbGenero.getSelectedItem().toString();
        boolean retorno = chbRetorno.isSelected();
        boolean menor = chbMenor.isSelected();
        
        if(listaTiquetes.isEmpty())
            JOptionPane.showMessageDialog(this, "No ha generado ningun Tiquete. \n1- Llene el formulario completo \n2- \"Generar Nuevo Tiquete\"  \n3- Añada a los pasajeros restantes \n4- \"Imprimir Tiquete\"");
        else{
            if(!nombre.equals("") && !apellido.equals("") && !tipoDocumento.equals("") && !numeroDocumento.equals("") && !sexo.equals("") ){
                    if(menor){
                        clsPasajero pasajeroNino = new clsPasajero(nombre, apellido, tipoDocumento, numeroDocumento, sexo);


                        LinkedList<clsPasajero> aux = listaTiquetes.getLast().getListaObjetosPasajero();
                            aux.add(pasajeroNino);   

                            listaTiquetes.getLast().setListaObjetosPasajero(aux);
                            JOptionPane.showMessageDialog(this, "Se añadio al pasajero en el tiquete exitosamente.");
                            limpiarFormularioPasajero();

                    }
                    else{
                        String contacto = txContacto.getText();
                        String correo = txCorreo.getText();
                        if(!contacto.equals("") && !correo.equals("")){
                            clsPasajeroAdulto pasajeroAdulto = new clsPasajeroAdulto(nombre, apellido, tipoDocumento, numeroDocumento, sexo, contacto, correo);



                            LinkedList<clsPasajeroAdulto> aux = listaTiquetes.getLast().getListaObjetosPasajeroAdulto();
                            aux.add(pasajeroAdulto);

                            listaTiquetes.getLast().setListaObjetosPasajeroAdulto(aux);
                            JOptionPane.showMessageDialog(this, "Se añadio al pasajero en el tiquete exitosamente.");
                            limpiarFormularioPasajero();


                        }
                        else
                            JOptionPane.showMessageDialog(this, "Ingrese los datos de contacto del mayor de edad");

                    }  
                }
            
                else
                    JOptionPane.showMessageDialog(this, "Ingrese todos los datos del viaje y del pasajero");
    }

        
    }//GEN-LAST:event_btAñadirPasajeroActionPerformed

