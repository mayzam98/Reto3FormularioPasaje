     public String imprimirTicket(clsTicket ticket){
         
         System.out.println("- - - - - - - - TIQUETE DE VIAJE - - - - - - ");
        double valorTotal = ticket.getValor()*(ticket.getCantidadAdultos()+ticket.getCantidadNiños());
        if(ticket.isRetorno())
            valorTotal = valorTotal *2;
        String informacionTicket = "- - - - - - - - - - -  - - - - - - - - - - -" + "\n" + "Lugar de salida: " + ticket.getSalida() + "       " + " Lugar de destino: " + ticket.getLlegada() + "\n" + "Fecha de salida: " + ticket.getFechaSalida()  + "    Hora salida: " + ticket.getHora() + "\n" + "Pasajeros adultos: " + ticket.getCantidadAdultos() +  " Pasajeros niños: "+ ticket.getCantidadNiños() + "\n" + "Ida y Vuelta: " + ticket.isRetorno() + "\n" + "Valor Unitario: " + ticket.getValor()  + "Valor Total: " + valorTotal +  "\n- - - - - - - - - - -  - - - - - - - - - - -" ;
        String informacionPasajeros = "";

        LinkedList<clsPasajero> listaObjetosPasajero =  ticket.getListaObjetosPasajero();
       
        for(clsPasajero pasajeroNino : listaObjetosPasajero){
            informacionPasajeros = "\n- - - - - - - - - - -  - - - - - - - - - - - \n" + informacionPasajeros + "\n" + "Pasajero: " + pasajeroNino.getNombre() + " " + pasajeroNino.getApellido() + "\nTipo de documento: " + pasajeroNino.getTipoDocumento() + "   " + " Numero de documento: " + pasajeroNino.getNumeroDocumento() +   "\nGenero: " + pasajeroNino.getSexo() + "\n- - - - - - - - - - -  - - - - - - - - - - - \n";
        }
        
        LinkedList<clsPasajeroAdulto> listaObjetosPasajeroAdulto =  ticket.getListaObjetosPasajeroAdulto();
        
        for(clsPasajeroAdulto pasajeroAdulto : listaObjetosPasajeroAdulto){
            informacionPasajeros ="\n- - - - - - - - - - -  - - - - - - - - - - - \n" + informacionPasajeros + "\n" + "Pasajero: " + pasajeroAdulto.getNombre() + " " + pasajeroAdulto.getApellido() + "\nTipo de documento: " + pasajeroAdulto.getTipoDocumento() + "   " + " Numero de documento: " + pasajeroAdulto.getNumeroDocumento() + "\nGenero: " + pasajeroAdulto.getSexo() + "\nCorreo Electrónico: " + pasajeroAdulto.getCorreoElectronico()+ "\nCelular:  " + pasajeroAdulto.getContacto() + "\n- - - - - - - - - - -  - - - - - - - - - - - \n";
        }
        
        
        
        return informacionTicket + informacionPasajeros;
    }
     
